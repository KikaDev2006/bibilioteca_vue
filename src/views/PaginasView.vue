<script setup lang="ts">
import { ref, reactive, computed, onMounted, watch, nextTick } from 'vue'
import { useRouter, useRoute } from 'vue-router'
import { api } from '@/config/api'
import Layout from '@/components/Layout.vue'
import type { Libro, AccionUsuarioIn } from '@/types'
import { useAuthStore } from '@/stores/auth'

const router = useRouter()
const route = useRoute()
const authStore = useAuthStore()

// Declarar las variables que faltan en el scope del template
// Ahora son refs reales en lugar de declaraciones

const paginas = ref<any[]>([])
const currentPageIndex = ref<number>(0)
const editingPage = ref<boolean>(false)
const saving = ref<boolean>(false)
const showAddModal = ref<boolean>(false)
const showEditModal = ref<boolean>(false)
const loading = ref<boolean>(false)
const selectedLibro = ref<Libro | null>(null)

const form = reactive({
  titulo: '',
  contenido: '',
  tipo: 'contenido',
})

const editForm = reactive({
  titulo: '',
  contenido: '',
  tipo: 'contenido',
})

// Computed para la p√°gina actual
const currentPage = computed(() => {
  return paginas.value[currentPageIndex.value] || null
})

// Variable para controlar si es la primera carga
const isInitialLoad = ref(true)

// Cargar libro y p√°ginas al montar
onMounted(async () => {
  const libroId = route.params.id
  if (libroId) {
    await loadLibro(Number(libroId))
    await loadPaginas(Number(libroId))
    
    console.log('=== DEBUG NAVEGACI√ìN ===')
    console.log('Usuario autenticado:', authStore.isAuthenticated)
    console.log('Libro cargado:', selectedLibro.value)
    console.log('P√°ginas cargadas:', paginas.value)
    console.log('IDs de p√°ginas:', paginas.value.map((p: any) => p.id))
    
    // Si el usuario est√° autenticado y el libro tiene √∫ltima p√°gina le√≠da, ir a esa p√°gina
    if (authStore.isAuthenticated && selectedLibro.value?.ultima_pagina_leida_id) {
      console.log('Intentando navegar a √∫ltima p√°gina le√≠da...')
      console.log('ID buscado:', selectedLibro.value.ultima_pagina_leida_id)
      
      // Buscar el √≠ndice de la p√°gina por su ID
      const paginaIndex = paginas.value.findIndex((p: any) => {
        console.log('Comparando:', p.id, '===', selectedLibro.value?.ultima_pagina_leida_id, '?', p.id === selectedLibro.value?.ultima_pagina_leida_id)
        return p.id === selectedLibro.value?.ultima_pagina_leida_id
      })
      
      console.log('√çndice encontrado:', paginaIndex)
      console.log('Total de p√°ginas:', paginas.value.length)
      
      if (paginaIndex >= 0) {
        console.log('‚úÖ Navegando a √≠ndice:', paginaIndex, '(p√°gina', paginaIndex + 1, ')')
        currentPageIndex.value = paginaIndex
      } else {
        console.log('‚ùå No se encontr√≥ la p√°gina con ID:', selectedLibro.value.ultima_pagina_leida_id)
      }
    } else {
      console.log('No se navega porque:')
      console.log('- Usuario autenticado:', authStore.isAuthenticated)
      console.log('- Tiene ultima_pagina_leida_id:', selectedLibro.value?.ultima_pagina_leida_id)
    }
    
    // Despu√©s de la carga inicial, habilitar el watch
    setTimeout(() => {
      isInitialLoad.value = false
    }, 500)
  }
})

// Watch para actualizar la √∫ltima p√°gina le√≠da cuando cambie la p√°gina actual
watch(currentPageIndex, async (newIndex) => {
  // No actualizar durante la carga inicial
  if (isInitialLoad.value) return
  
  if (authStore.isAuthenticated && selectedLibro.value && paginas.value[newIndex]) {
    const paginaActual = paginas.value[newIndex] as any
    console.log('Actualizando √∫ltima p√°gina le√≠da - N√∫mero:', newIndex + 1, 'ID:', paginaActual.id)
    await updateUltimaPaginaLeida(newIndex + 1, paginaActual.id)
  }
})

// Cargar informaci√≥n del libro
const loadLibro = async (libroId: number) => {
  try {
    // Si el usuario est√° autenticado, usar el endpoint que incluye acciones
    const endpoint = authStore.isAuthenticated 
      ? `/libro/todos-autenticado` 
      : `/libro/${libroId}`
    
    if (authStore.isAuthenticated) {
      // Cargar todos los libros y buscar el espec√≠fico
      const response = await api.get(endpoint)
      const libro = response.data.find((l: any) => l.id === libroId)
      if (libro) {
        selectedLibro.value = libro
        console.log('Libro cargado con acciones:', libro)
        console.log('ultima_pagina_leida_id:', libro.ultima_pagina_leida_id)
      } else {
        // Fallback al endpoint normal
        const fallbackResponse = await api.get(`/libro/${libroId}`)
        selectedLibro.value = fallbackResponse.data
      }
    } else {
      const response = await api.get(endpoint)
      selectedLibro.value = response.data
    }
  } catch (error) {
    console.error('Error cargando libro:', error)
  }
}

// Cargar p√°ginas del libro
const loadPaginas = async (libroId: number) => {
  try {
    loading.value = true
    const response = await api.get(`/libro/${libroId}/paginas`)
    paginas.value = response.data
  } catch (error) {
    console.error('Error cargando p√°ginas:', error)
  } finally {
    loading.value = false
  }
}

// Volver a la biblioteca
const goBack = () => {
  router.push('/')
}

// Funci√≥n para actualizar la √∫ltima p√°gina le√≠da
const updateUltimaPaginaLeida = async (numeroPagina: number, paginaId: number) => {
  if (!selectedLibro.value) return
  
  try {
    const accionData: AccionUsuarioIn = {
      libro_id: selectedLibro.value.id,
      es_favorito: selectedLibro.value.es_favorito || false,
      pendiente_leer: selectedLibro.value.pendiente_leer || false,
      ultima_pagina_leida_id: paginaId,
      calificacion: 0
    }
    
    // Intentar actualizar primero
    try {
      await api.put(`/acciones_usuario/libro/${selectedLibro.value.id}`, accionData)
    } catch (error: any) {
      // Si no existe (404), crear nueva acci√≥n
      if (error.response?.status === 404) {
        await api.post('/acciones_usuario/', accionData)
      } else {
        throw error
      }
    }
    
    // Actualizar el estado local
    if (selectedLibro.value) {
      selectedLibro.value.ultima_pagina_leida = numeroPagina
      selectedLibro.value.ultima_pagina_leida_id = paginaId
    }
  } catch (error) {
    console.error('Error actualizando √∫ltima p√°gina le√≠da:', error)
  }
}

// Navegaci√≥n de p√°ginas con animaci√≥n suave
const nextPage = () => {
  if (currentPageIndex.value < paginas.value.length - 1) {
    currentPageIndex.value++
    scrollToTop()
  }
}

const previousPage = () => {
  if (currentPageIndex.value > 0) {
    currentPageIndex.value--
    scrollToTop()
  }
}

// Funci√≥n para hacer scroll suave al principio de la p√°gina
const scrollToTop = () => {
  nextTick(() => {
    const pageContent = document.querySelector('.page-content')
    if (pageContent) {
      pageContent.scrollTo({ top: 0, behavior: 'smooth' })
    }
  })
}

const goToPage = () => {
  // La p√°gina se cambia autom√°ticamente con v-model
}

// Iniciar edici√≥n inline
const startEditing = () => {
  if (currentPage.value) {
    editingPage.value = true
    editForm.titulo = currentPage.value.titulo || `P√°gina ${currentPageIndex.value + 1}`
    editForm.contenido = currentPage.value.contenido
    editForm.tipo = currentPage.value.tipo
  }
}

// Guardar edici√≥n
const saveEdit = async () => {
  if (!currentPage.value) return

  try {
    saving.value = true
    const updateData = {
      titulo: editForm.titulo,
      contenido: editForm.contenido,
      tipo: editForm.tipo,
      libro_id: selectedLibro.value!.id,
    }

    await api.put(`/pagina/${currentPage.value.id}`, updateData)
    await loadPaginas(selectedLibro.value!.id)
    editingPage.value = false
  } catch (error) {
    console.error('Error guardando p√°gina:', error)
    alert('Error al guardar la p√°gina')
  } finally {
    saving.value = false
  }
}

// Cancelar edici√≥n
const cancelEdit = () => {
  editingPage.value = false
  editForm.titulo = ''
  editForm.contenido = ''
  editForm.tipo = 'contenido'
}

// Eliminar p√°gina
const deletePagina = async (paginaId: number) => {
  if (confirm('¬øEst√°s seguro de que quieres eliminar esta p√°gina?')) {
    try {
      await api.delete(`/pagina/${paginaId}`)
      await loadPaginas(selectedLibro.value!.id)
    } catch (error) {
      console.error('Error eliminando p√°gina:', error)
      alert('Error al eliminar la p√°gina')
    }
  }
}

// Manejar env√≠o del formulario (solo para crear nuevas p√°ginas)
const handleSubmit = async () => {
  try {
    // Crear nueva p√°gina
    const newPagina = {
      titulo: form.titulo,
      contenido: form.contenido,
      tipo: form.tipo,
      libro_id: selectedLibro.value!.id,
    }

    await api.post('/pagina/', newPagina)
    closeModal()
    await loadPaginas(selectedLibro.value!.id)
  } catch (error) {
    console.error('Error guardando p√°gina:', error)
    alert('Error al guardar la p√°gina')
  }
}

// Cerrar modal
const closeModal = () => {
  showAddModal.value = false
  showEditModal.value = false
  form.titulo = ''
  form.contenido = ''
  form.tipo = 'contenido'
}
</script>

